package Controllers;

public class Account {
    private int subNo;
    private String name;
    private String email;
    private String adress;
    private String residence;


    public Account(int subNo, String name, String email, String adress, String residence) {
        this.subNo = subNo;
        this.name = name;
        this.email = email;
        this.adress = adress;
        this.residence = residence;

    }
}
